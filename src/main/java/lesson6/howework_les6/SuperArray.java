    private static void processArrayElements(String[][] array) throws MyArraySizeException, MyArrayDataException {
        int sum = 0;
        if (array.length != 4) {//–ø—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ–ª-–≤–æ —Å—Ç—Ä–æ–∫
            System.err.println("–ù–µ–≤–µ—Ä–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫. –û–∂–∏–¥–∞–ª–æ—Å—å 4, –ø–æ–ª—É—á–µ–Ω–æ " + array.length);
            throw new MyArraySizeException();
        }
        for (int i = 0; i < array.length; i++) {//–ø—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ–ª-–≤–æ —Å–∏–º–≤–æ–ª–æ–≤ –≤ —Å—Ç—Ä–æ–∫–µ
            if (array[i].length != 4) {
                System.err.println("–ù–µ–≤–µ—Ä–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–∏–º–≤–æ–ª–æ–≤ –≤ —Å—Ç—Ä–æ–∫–µ " + i + ". –û–∂–∏–¥–∞–ª–æ—Å—å 4, –ø–æ–ª—É—á–µ–Ω–æ " + array[i].length);
                throw new MyArraySizeException();
            }
            for (int j = 0; j < array[i].length; j++) {
                try {
                    int num = Integer.parseInt(array[i][j]);
                    sum += num;
                } catch (NumberFormatException exception) {
                    System.err.println("–û—à–∏–±–∫–∞ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è –≤ —Å—Ç—Ä–æ–∫–µ " + i + ", —Å—Ç–æ–ª–±—Ü–µ " + j + ": " + exception.getMessage());
                    throw new MyArrayDataException(i, j);
                }
            }
        }
        System.out.println("–°—É–º–º–∞ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –º–∞—Å—Å–∏–≤–∞: " + sum);
    }

