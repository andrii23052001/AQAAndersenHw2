package homework;

/*1. Написать метод, принимающий на вход два целых числа и проверяющий,
        что их сумма лежит в пределах от 10 до 20 (включительно),
        если да – вернуть true, в противном случае – false.*/
public class TwoNumbersTask1 {
    public static void main(String[] args) {
        // Вызывается метод sumFrom10To20 с аргументами 15 и 5
        sumFrom10To20(15, 5);
    }

    // Метод проверяет, лежит ли сумма двух целых чисел в пределах от 10 до 20 включительно
    // и выводит соответствующее сообщение в консоль
    public static boolean sumFrom10To20(int a, int b) {
        // Вычисляется сумма чисел
        int sum = a + b;

        // Проверка условия: сумма должна быть больше 10 и меньше либо равна 20
        if (sum > 10 && sum <= 20) {
            // Если условие выполняется, выводится сообщение
            System.out.println("Сумма лежит в пределах от 10 до 20 (включительно)");
            // Возвращается значение true, указывающее на соответствие условию
            return true;
        } else {
            // Если условие не выполняется, выводится другое сообщение
            System.out.println("Сумма не лежит в пределах от 10 до 20 (включительно)");
            // Возвращается значение false, указывающее на несоответствие условию
            return false;
        }
    }
}