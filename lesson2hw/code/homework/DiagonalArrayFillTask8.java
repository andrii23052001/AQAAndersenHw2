package homework;

import java.util.Arrays;

/*8. Создать квадратный двумерный целочисленный массив
        (количество строк и столбцов одинаковое), и с помощью цикла
        (-ов) заполнить его диагональные элементы единицами
        (можно только одну из диагоналей, если обе сложно).
        Определить элементы одной из диагоналей можно по следующему принципу:
        индексы таких элементов равны,
        то есть [0][0], [1][1], [2][2], …, [n][n];*/
public class DiagonalArrayFillTask8 {
    public static void main(String[] args) {
        int size = 5; // Задаем размер квадратного массива
        int[][] array = new int[size][size]; // Создаем двумерный массив

        // Заполняем обе диагонали единицами
        for (int i = 0; i < size; i++) {
            array[i][i] = 1;                // Главная диагональ
            array[i][size - 1 - i] = 1;     // Побочная диагональ
        }

        // Выводим массив на экран
        for (int i = 0; i < size; i++) {
            System.out.println(Arrays.toString(array[i]));
        }
    }
}